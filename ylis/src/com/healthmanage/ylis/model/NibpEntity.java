package com.healthmanage.ylis.model;

public class NibpEntity {
	private String inputDate;
	private String sbpVal;
	private String dbpVal;
	private String prVal;
	
	public String getInputDate() {
		return inputDate;
	}
	public void setInputDate(String inputDate) {
		this.inputDate = inputDate;
	}
	public String getSbpVal() {
		return sbpVal;
	}
	public void setSbpVal(String sbpVal) {
		this.sbpVal = sbpVal;
	}
	public String getDbpVal() {
		return dbpVal;
	}
	public void setDbpVal(String dbpVal) {
		this.dbpVal = dbpVal;
	}
	public String getPrVal() {
		return prVal;
	}
	public void setPrVal(String prVal) {
		this.prVal = prVal;
	}

	
}
