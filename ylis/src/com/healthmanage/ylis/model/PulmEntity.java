package com.healthmanage.ylis.model;

public class PulmEntity {
	private String inputDate;
	private String fvcVal;
	private String fevVal;
	private String pefVal;
	public String getInputDate() {
		return inputDate;
	}
	public void setInputDate(String inputDate) {
		this.inputDate = inputDate;
	}
	public String getFvcVal() {
		return fvcVal;
	}
	public void setFvcVal(String fvcVal) {
		this.fvcVal = fvcVal;
	}
	public String getFevVal() {
		return fevVal;
	}
	public void setFevVal(String fevVal) {
		this.fevVal = fevVal;
	}
	public String getPefVal() {
		return pefVal;
	}
	public void setPefVal(String pefVal) {
		this.pefVal = pefVal;
	}
	
	
}
